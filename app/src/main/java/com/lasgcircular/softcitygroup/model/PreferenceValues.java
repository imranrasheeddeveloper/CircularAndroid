package com.lasgcircular.softcitygroup.model;

public class PreferenceValues {

    String selectedIndustry, teamSize, projectBudget , reportImpact;

    public PreferenceValues() {
    }

    public String getSelectedIndustry() {
        return selectedIndustry;
    }

    public void setSelectedIndustry(String selectedIndustry) {
        this.selectedIndustry = selectedIndustry;
    }

    public String getTeamSize() {
        return teamSize;
    }

    public void setTeamSize(String teamSize) {
        this.teamSize = teamSize;
    }

    public String getProjectBudget() {
        return projectBudget;
    }

    public void setProjectBudget(String projectBudget) {
        this.projectBudget = projectBudget;
    }

    public String getReportImpact() {
        return reportImpact;
    }

    public void setReportImpact(String reportImpact) {
        this.reportImpact = reportImpact;
    }
}
